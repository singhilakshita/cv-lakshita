// Emits src/data/projectStats.ts from each app repo's settings.gradle.kts +
// Room DB file, fetched over raw.githubusercontent (public repos, no auth;
// optional GITHUB_TOKEN lifts the rate limit for the screenshot count).
// If ANY fetch fails, the committed projectStats.ts is left untouched so
// offline / Vercel-hiccup builds still succeed off the last good copy.
import { writeFileSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const outFile = join(root, "src", "data", "projectStats.ts");
const token = process.env.GITHUB_TOKEN;

const raw = (repo, ref, path) => `https://raw.githubusercontent.com/${repo}/${ref}/${path}`;
const contents = (repo, ref, path) => `https://api.github.com/repos/${repo}/contents/${path}?ref=${ref}`;

async function getText(url) {
  const res = await fetch(url, { headers: token ? { Authorization: `Bearer ${token}` } : {} });
  if (!res.ok) throw new Error(`${res.status} ${url}`);
  return res.text();
}

async function pngCount(repo, ref, path) {
  const res = await fetch(contents(repo, ref, path), {
    headers: { Accept: "application/vnd.github+json", ...(token ? { Authorization: `Bearer ${token}` } : {}) },
  });
  if (!res.ok) throw new Error(`${res.status} ${contents(repo, ref, path)}`);
  const list = await res.json();
  return list.filter((f) => /\.png$/i.test(f.name)).length;
}

const count = (s, re) => (s.match(re) || []).length;

async function build() {
  const mRepo = "darkpandawarrior/Mileway";
  const pRepo = "darkpandawarrior/PaymentsLab";
  const kRepo = "darkpandawarrior/Kursi";

  const mSettings = await getText(raw(mRepo, "main", "settings.gradle.kts"));
  const mDb = await getText(raw(mRepo, "main", "core/data/src/commonMain/kotlin/com/mileway/core/data/database/MilewayDatabase.kt"));
  const pSettings = await getText(raw(pRepo, "main", "settings.gradle.kts"));
  const kSettings = await getText(raw(kRepo, "main", "settings.gradle.kts"));

  const dbMatch = mDb.match(/version\s*=\s*(\d+)/);
  if (!dbMatch) throw new Error("could not parse Mileway DB version");

  return {
    mileway: {
      modules: count(mSettings, /^include\(/gm),
      features: count(mSettings, /^include\(":feature:/gm),
      cores: count(mSettings, /^include\(":core:/gm),
      dbVersion: Number(dbMatch[1]),
      screenshots: await pngCount(mRepo, "main", "docs/screenshots"),
    },
    paymentslab: {
      modules: count(pSettings, /^include\(/gm),
      providers: count(pSettings, /^include\(":provider:/gm),
      features: count(pSettings, /^include\(":feature:/gm),
      cores: count(pSettings, /^include\(":core:/gm),
      screenshots: await pngCount(pRepo, "main", "docs/screenshots"),
    },
    kursi: {
      modules: count(kSettings, /^include\(/gm),
      screenshots: await pngCount(kRepo, "main", "docs/screenshots"),
    },
  };
}

const banner =
  "// AUTO-GENERATED by scripts/gen-project-stats.mjs — do not edit by hand.\n" +
  "// Numbers are derived from each app repo's settings.gradle.kts + Room DB over\n" +
  "// raw.githubusercontent. Run `npm run gen:stats` to refresh.\n";

try {
  const stats = await build();
  // Sanity guard: a parse that silently returns 0 modules is a bad fetch, not real.
  if (!stats.mileway.modules || !stats.paymentslab.modules || !stats.kursi.modules) throw new Error("parsed 0 modules — refusing to overwrite");
  writeFileSync(outFile, banner + `export const projectStats = ${JSON.stringify(stats, null, 2)} as const;\n`);
  console.log("[gen-project-stats]", JSON.stringify(stats));
} catch (err) {
  if (existsSync(outFile)) {
    console.warn("[gen-project-stats] fetch failed, keeping committed projectStats.ts —", err.message);
  } else {
    console.error("[gen-project-stats] fetch failed and no committed projectStats.ts exists —", err.message);
    process.exit(1);
  }
}
