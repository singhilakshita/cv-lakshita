// Scans public/projects/<slug>/screenshots and emits src/data/galleries.ts.
// Runs as a prebuild step, so adding/removing a screenshot auto-updates the
// project detail galleries — no manual list to maintain.
import { readdirSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const projectsDir = join(root, "public", "projects");
const out = {};

if (existsSync(projectsDir)) {
  for (const slug of readdirSync(projectsDir)) {
    const shots = join(projectsDir, slug, "screenshots");
    if (existsSync(shots)) {
      out[slug] = readdirSync(shots)
        .filter((f) => /\.(png|jpe?g|webp|gif)$/i.test(f))
        .sort()
        .map((f) => `/projects/${slug}/screenshots/${f}`);
    }
  }
}

const banner =
  "// AUTO-GENERATED by scripts/gen-galleries.mjs — do not edit by hand.\n" +
  "// Add images under public/projects/<slug>/screenshots/ and they appear automatically.\n";
writeFileSync(
  join(root, "src", "data", "galleries.ts"),
  banner + `export const galleries: Record<string, string[]> = ${JSON.stringify(out, null, 2)};\n`,
);
console.log(
  "[gen-galleries]",
  Object.fromEntries(Object.entries(out).map(([k, v]) => [k, v.length])),
);
